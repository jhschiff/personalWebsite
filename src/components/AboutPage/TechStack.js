const TechStack = () => {
  const getIcon = (category) => {
    switch (category) {
      case 'languages':
        return <CodeIcon />;
      case 'frontend':
        return <WebIcon />;
      case 'backend':
        return <ServerIcon />;
      case 'devops':
        return <ToolsIcon />;
      default:
        return null;
    }
  };

  return (
    <section className="pt-20 pb-0">
      <div className="about-container">
        <motion.h3
          initial={{ opacity: 0, y: 20 }}
          whileInView={{ opacity: 1, y: 0 }}
          viewport={{ once: true }}
          className="tech-stack-title"
        >
          Tech Stack
        </motion.h3>

        <div className="tech-stack-grid">
          {Object.entries(TECH_STACK).map(([category, data], index) => (
            <motion.div
              key={category}
              initial={{ opacity: 0, y: 20 }}
              whileInView={{ opacity: 1, y: 0 }}
              viewport={{ once: true }}
              transition={{ delay: 0.7 + index * 0.1 }}
              className="tech-stack-card"
            >
              <h4 className="tech-stack-card-title">
                {getIcon(category)}
                {data.title}
              </h4>
              <ul className="tech-stack-list">
                {data.items.map((item, i) => (
                  <li key={i}>{item}</li>
                ))}
              </ul>
              <p className="tech-stack-description">
                {data.description}
              </p>
            </motion.div>
          ))}
        </div>
      </div>
    </section>
  );
};

export default TechStack; 